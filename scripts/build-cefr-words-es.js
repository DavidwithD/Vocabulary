const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const ELELEX_PATH = path.join(ROOT, 'sources', 'ELELex.tsv');
const OUTPUT_PATH = path.join(ROOT, 'data', 'cefr-words-es.js');

const LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'];

// Accept single words with letters and accented characters (no compounds, numbers)
function isValidWord(w) {
  return /^[a-zA-ZÀ-ÖØ-öø-ÿñÑœŒæÆ]{2,}$/u.test(w);
}

// ── Parse ELELex TSV ────────────────────────────────────────────
// Columns (quoted): "word", "tag", "level_freq@a1", "level_freq@a2",
//   "level_freq@b1", "level_freq@b2", "level_freq@c1", "total_freq@total", ...
// Note: ELELex only covers A1–C1 (no C2 column).
// Same word may appear multiple times with different POS tags.
// We aggregate: for each unique word, sum frequencies per level across all POS rows,
// then assign to the lowest level where frequency meets threshold.

function parseELELex() {
  const raw = fs.readFileSync(ELELEX_PATH, 'utf8');
  const lines = raw.split('\n');
  const header = lines[0].split('\t').map(h => h.replace(/"/g, ''));

  // Map CEFR levels to ELELex column names (lowercase, no C2 in source)
  const levelColMap = {
    A1: 'level_freq@a1',
    A2: 'level_freq@a2',
    B1: 'level_freq@b1',
    B2: 'level_freq@b2',
    C1: 'level_freq@c1',
  };

  const freqCols = {};
  for (const [lvl, colName] of Object.entries(levelColMap)) {
    const idx = header.indexOf(colName);
    if (idx === -1) throw new Error(`Column ${colName} not found in ELELex header`);
    freqCols[lvl] = idx;
  }

  // Aggregate frequencies per word across all POS rows
  const wordFreqs = new Map(); // word -> { A1: sum, A2: sum, ... }

  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split('\t');
    if (cols.length < 7) continue;

    const word = cols[0].replace(/"/g, '').toLowerCase().trim();
    if (!isValidWord(word)) continue;

    if (!wordFreqs.has(word)) {
      wordFreqs.set(word, { A1: 0, A2: 0, B1: 0, B2: 0, C1: 0 });
    }
    const entry = wordFreqs.get(word);
    for (const lvl of ['A1', 'A2', 'B1', 'B2', 'C1']) {
      const val = parseFloat(cols[freqCols[lvl]].replace(/"/g, '')) || 0;
      entry[lvl] += val;
    }
  }

  // Assign each word to the lowest level where frequency meets threshold
  const THRESHOLD = 5;
  const result = new Map(); // word -> level

  for (const [word, freqs] of wordFreqs) {
    let assignedLevel = null;

    for (const lvl of ['A1', 'A2', 'B1', 'B2', 'C1']) {
      if (freqs[lvl] >= THRESHOLD) {
        assignedLevel = lvl;
        break;
      }
    }

    // If no level meets threshold, assign to C2 (very rare words)
    if (!assignedLevel) {
      assignedLevel = 'C2';
    }

    result.set(word, assignedLevel);
  }

  return result;
}

// ── Build and output ────────────────────────────────────────────

function build() {
  console.log('Parsing ELELex...');
  const words = parseELELex();
  console.log(`  ${words.size} unique words from ELELex`);

  // Group by level
  const groups = {};
  LEVELS.forEach(lvl => { groups[lvl] = []; });

  for (const [word, level] of words) {
    groups[level].push(word);
  }

  // Sort each level alphabetically
  LEVELS.forEach(lvl => groups[lvl].sort());

  // Print stats
  console.log('\nWords per level:');
  LEVELS.forEach(lvl => {
    console.log(`  ${lvl}: ${groups[lvl].length}`);
  });

  // Write output file
  const outputLines = [
    '// Auto-generated by scripts/build-cefr-words-es.js',
    '// Source: ELELex (UCLouvain, CC BY-NC-SA 4.0)',
    '// eslint-disable-next-line',
    'const CEFR_WORDS_ES = {',
  ];

  LEVELS.forEach((lvl, i) => {
    const wordList = groups[lvl].map(w => `'${w.replace(/'/g, "\\'")}'`).join(',');
    const comma = i < LEVELS.length - 1 ? ',' : '';
    outputLines.push(`  ${lvl}: [${wordList}]${comma}`);
  });

  outputLines.push('};');
  outputLines.push('');

  fs.writeFileSync(OUTPUT_PATH, outputLines.join('\n'), 'utf8');
  console.log(`\nWritten to ${OUTPUT_PATH}`);
}

build();
