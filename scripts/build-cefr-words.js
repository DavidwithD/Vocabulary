const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const EFLLEX_PATH = path.join(ROOT, 'EFLLex.tsv');
const OCTANOVE_PATH = path.join(ROOT, 'octanove-vocabulary-profile-c1c2-1.0.csv');
const OUTPUT_PATH = path.join(ROOT, 'cefr-words.js');

const LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1'];
const ALL_LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'];

// Only keep single alphabetic words (no compounds, numbers, abbreviations)
function isValidWord(w) {
  return /^[a-zA-Z]{2,}$/.test(w);
}

// ── Parse EFLLex TSV ──────────────────────────────────────────────
// Columns: word, tag, level_freq@a1, level_freq@a2, level_freq@b1, level_freq@b2, level_freq@c1, ...
// Same word may appear multiple times with different POS tags.
// We aggregate: for each unique word, sum frequencies per level across all POS rows,
// then assign to the level with the highest total frequency.

function parseEFLLex() {
  const raw = fs.readFileSync(EFLLEX_PATH, 'utf8');
  const lines = raw.split('\n');
  const header = lines[0].split('\t');

  // Find column indices for level frequencies
  const freqCols = {};
  LEVELS.forEach(lvl => {
    const colName = `level_freq@${lvl.toLowerCase()}`;
    const idx = header.indexOf(colName);
    if (idx === -1) throw new Error(`Column ${colName} not found in EFLLex header`);
    freqCols[lvl] = idx;
  });

  // Aggregate frequencies per word across all POS rows
  const wordFreqs = new Map(); // word -> { A1: sum, A2: sum, ... }

  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split('\t');
    if (cols.length < 7) continue;

    const word = cols[0].toLowerCase().trim();
    if (!isValidWord(word)) continue;

    if (!wordFreqs.has(word)) {
      wordFreqs.set(word, { A1: 0, A2: 0, B1: 0, B2: 0, C1: 0 });
    }
    const entry = wordFreqs.get(word);
    LEVELS.forEach(lvl => {
      const val = parseFloat(cols[freqCols[lvl]]) || 0;
      entry[lvl] += val;
    });
  }

  // Assign each word to the level with the highest aggregated frequency
  const result = new Map(); // word -> level
  for (const [word, freqs] of wordFreqs) {
    let bestLevel = 'C1';
    let bestFreq = 0;
    for (const lvl of LEVELS) {
      if (freqs[lvl] > bestFreq) {
        bestFreq = freqs[lvl];
        bestLevel = lvl;
      }
    }
    result.set(word, bestLevel);
  }

  return result;
}

// ── Parse OLP Octanove C1/C2 CSV ─────────────────────────────────
// Columns: headword, pos, CEFR, notes
// Keep only C2 words (C1 words are already in EFLLex).
// For words appearing at multiple levels, keep the lowest.

function parseOctanove() {
  const raw = fs.readFileSync(OCTANOVE_PATH, 'utf8');
  const lines = raw.split('\n').slice(1); // skip header
  const result = new Map(); // word -> level

  const levelOrder = { C1: 0, C2: 1 };

  for (const line of lines) {
    if (!line.trim()) continue;
    const cols = line.split(',');
    const word = cols[0].toLowerCase().trim();
    const level = cols[2].trim();

    if (!isValidWord(word)) continue;
    if (level !== 'C1' && level !== 'C2') continue;

    if (!result.has(word) || levelOrder[level] < levelOrder[result.get(word)]) {
      result.set(word, level);
    }
  }

  return result;
}

// ── Merge and output ──────────────────────────────────────────────

function build() {
  console.log('Parsing EFLLex...');
  const efllexWords = parseEFLLex();
  console.log(`  ${efllexWords.size} unique single words from EFLLex`);

  console.log('Parsing Octanove C1/C2...');
  const octanoveWords = parseOctanove();
  console.log(`  ${octanoveWords.size} words from Octanove`);

  // Merge: EFLLex is primary, Octanove supplements with words not already covered
  const merged = new Map(efllexWords);
  let added = 0;
  for (const [word, level] of octanoveWords) {
    if (!merged.has(word)) {
      merged.set(word, level);
      added++;
    }
  }
  console.log(`  ${added} new words added from Octanove`);
  console.log(`  ${merged.size} total unique words`);

  // Group by level
  const groups = {};
  ALL_LEVELS.forEach(lvl => { groups[lvl] = []; });

  for (const [word, level] of merged) {
    groups[level].push(word);
  }

  // Sort each level alphabetically
  ALL_LEVELS.forEach(lvl => groups[lvl].sort());

  // Print stats
  console.log('\nWords per level:');
  ALL_LEVELS.forEach(lvl => {
    console.log(`  ${lvl}: ${groups[lvl].length}`);
  });

  // Write output file
  const lines = [
    '// Auto-generated by scripts/build-cefr-words.js',
    '// Sources: EFLLex (UCLouvain, CC BY-NC-SA 4.0) + Octanove Vocabulary Profile (CC BY-SA 4.0)',
    '// eslint-disable-next-line',
    'const CEFR_WORDS = {',
  ];

  ALL_LEVELS.forEach((lvl, i) => {
    const words = groups[lvl].map(w => `'${w.replace(/'/g, "\\'")}'`).join(',');
    const comma = i < ALL_LEVELS.length - 1 ? ',' : '';
    lines.push(`  ${lvl}: [${words}]${comma}`);
  });

  lines.push('};');
  lines.push('// eslint-disable-next-line');
  lines.push("const CEFR_LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'];");
  lines.push('');

  fs.writeFileSync(OUTPUT_PATH, lines.join('\n'), 'utf8');
  console.log(`\nWritten to ${OUTPUT_PATH}`);
}

build();
