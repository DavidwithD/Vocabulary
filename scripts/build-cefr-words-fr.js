const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const FLELEX_PATH = path.join(ROOT, 'sources', 'FLELex.csv');
const OUTPUT_PATH = path.join(ROOT, 'data', 'cefr-words-fr.js');

const LEVELS = ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'];

// Accept single words with letters and accented characters (no compounds, numbers)
function isValidWord(w) {
  return /^[a-zA-ZÀ-ÖØ-öø-ÿœŒæÆ]{2,}$/u.test(w);
}

// ── Parse FLELex CSV (tab-separated) ────────────────────────────
// Columns: word, tag, freq_A1, freq_A2, freq_B1, freq_B2, freq_C1, freq_C2, freq_total
// Same word may appear multiple times with different POS tags.
// We aggregate: for each unique word, sum frequencies per level across all POS rows,
// then assign to the level with the highest total frequency.

function parseFLELex() {
  const raw = fs.readFileSync(FLELEX_PATH, 'utf8');
  const lines = raw.split('\n');
  const header = lines[0].split('\t');

  // Find column indices for level frequencies
  const freqCols = {};
  LEVELS.forEach(lvl => {
    const colName = `freq_${lvl}`;
    const idx = header.indexOf(colName);
    if (idx === -1) throw new Error(`Column ${colName} not found in FLELex header`);
    freqCols[lvl] = idx;
  });

  // Aggregate frequencies per word across all POS rows
  const wordFreqs = new Map(); // word -> { A1: sum, A2: sum, ... }

  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split('\t');
    if (cols.length < 9) continue;

    const word = cols[0].toLowerCase().trim();
    if (!isValidWord(word)) continue;

    if (!wordFreqs.has(word)) {
      wordFreqs.set(word, { A1: 0, A2: 0, B1: 0, B2: 0, C1: 0, C2: 0 });
    }
    const entry = wordFreqs.get(word);
    LEVELS.forEach(lvl => {
      const val = parseFloat(cols[freqCols[lvl]]) || 0;
      entry[lvl] += val;
    });
  }

  // Assign each word to the level with the highest aggregated frequency
  const result = new Map(); // word -> level
  for (const [word, freqs] of wordFreqs) {
    let bestLevel = 'C2';
    let bestFreq = 0;
    for (const lvl of LEVELS) {
      if (freqs[lvl] > bestFreq) {
        bestFreq = freqs[lvl];
        bestLevel = lvl;
      }
    }
    result.set(word, bestLevel);
  }

  return result;
}

// ── Build and output ────────────────────────────────────────────

function build() {
  console.log('Parsing FLELex...');
  const words = parseFLELex();
  console.log(`  ${words.size} unique words from FLELex`);

  // Group by level
  const groups = {};
  LEVELS.forEach(lvl => { groups[lvl] = []; });

  for (const [word, level] of words) {
    groups[level].push(word);
  }

  // Sort each level alphabetically
  LEVELS.forEach(lvl => groups[lvl].sort());

  // Print stats
  console.log('\nWords per level:');
  LEVELS.forEach(lvl => {
    console.log(`  ${lvl}: ${groups[lvl].length}`);
  });

  // Write output file
  const outputLines = [
    '// Auto-generated by scripts/build-cefr-words-fr.js',
    '// Source: FLELex (UCLouvain, CC BY-NC-SA 4.0)',
    '// eslint-disable-next-line',
    'const CEFR_WORDS_FR = {',
  ];

  LEVELS.forEach((lvl, i) => {
    const wordList = groups[lvl].map(w => `'${w.replace(/'/g, "\\'")}'`).join(',');
    const comma = i < LEVELS.length - 1 ? ',' : '';
    outputLines.push(`  ${lvl}: [${wordList}]${comma}`);
  });

  outputLines.push('};');
  outputLines.push('');

  fs.writeFileSync(OUTPUT_PATH, outputLines.join('\n'), 'utf8');
  console.log(`\nWritten to ${OUTPUT_PATH}`);
}

build();
